function multiply(){
	var n1=document.getElementById("n1").value;
	var n2=document.getElementById("n2").value;
	document.getElementById("p").innerHTML= ""+n1+" * "+n2+" = "+(n1*n2);
}
function divide(){
	var n1=document.getElementById("n1").value;
	var n2=document.getElementById("n2").value;
	document.getElementById("p").innerHTML= ""+n1+" / "+n2+" = "+(n1/n2);
}
